from sqlalchemy import (
    Column, Integer, ForeignKey, Table
)

from db import Base

race_divisions = Table(
    'race_divisions', Base.metadata,
    Column('race_id', Integer, ForeignKey('races.id', ondelete="CASCADE"), primary_key=True),
    Column('division_id', Integer, ForeignKey('divisions.id', ondelete="CASCADE"), primary_key=True)
)
